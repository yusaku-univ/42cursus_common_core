#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	count;
	size_t	i;
	size_t	j;
	char	*result;

	count = 0;
	count += ft_strlen(s1);
	count += ft_strlen(s2);
	result = (char *)malloc(sizeof(char) * (count + 1));
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		result[i + j] = s2[j];
		j++;
	}
	result[i + j] = '\0';
	return (result);
}
